package Notes;

public class stringConcatenation {

	public static void main(String[] args) {
		
		String str1 = "Hello ";
		String str2 = "World!";
		String str3 = str1 + str2;
		System.out.println(str3);
		
		//Unique Cases of String Concatenation
		System.out.println(15 + "0 + 45" + 96 + 2);
		
		System.out.println(9 + 3 + 1 + "6 - 21" + 77 + 0);
		
		System.out.println(3 + 7 * 2 + "78 - 9" + (3+1) / 5);
		
		System.out.println("Hello" + '1' + 5 + false + 3.14);
		
	}

}
